static int get_tbl_index(void *tstart, void *parm,
			 u32 tsize, u32 tnum)
{
	int i;

	/* search if the entry is already available and can be re-used */
	for (i = 0; i < tnum; i++) {
		/* entry is used, check if the entry content fits */
		if (memcmp(((char *)tstart)
			   + i * tsize, parm, (u8)tsize) == 0)
			return i;
	}

	return 0xFF;
}

